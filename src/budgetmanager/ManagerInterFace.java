    private static String readUrl(String urlString) throws Exception {
    BufferedReader reader = null;
    try {
        URL url = new URL(urlString);
        reader = new BufferedReader(new InputStreamReader(url.openStream()));
        StringBuffer buffer = new StringBuffer();
        int read;
        char[] chars = new char[1024];
        while ((read = reader.read(chars)) != -1)
            buffer.append(chars, 0, read); 

        return buffer.toString();
    } finally {
        if (reader != null)
            reader.close();
        }
    }
    
    public double getConv(String cur, boolean renew)
    {
        try
        {
            if(renew)
            {
                convjs = readUrl("http://openexchangerates.org/api/latest.json?app_id=3c1c4ec3911e43e0b459aecde6be730a");
            }
            String[] v = convjs.split(",");
            for(int i=0; i<v.length; i++)
            {
                if(v[i].contains(cur))
                {
                    return Double.parseDouble(v[i].split(": ")[1]);
                }
            }
            return 1.0;
        }catch(Exception e)
        {
            e.printStackTrace();
            return 1.0;
        }
    }

